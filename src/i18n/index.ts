const interpolate = (message: string, params?: Params) => {
  if (!params) {
    return message;
  }

  return Object.entries(params).reduce(
    (result, [key, value]) => result.replaceAll(`{{${key}}}`, String(value)),
    message,
  );
};

type LanguageState = {
  language: Language;
  initialized: boolean;
  initLanguage: () => Promise<void>;
  setLanguage: (language: Language) => Promise<void>;
};

export const useLanguageStore = create<LanguageState>((set) => ({
  language: 'vi',
  initialized: false,
  initLanguage: async () => {
    const storedLanguage = await AsyncStorage.getItem(LANGUAGE_KEY);
    set({
      language: storedLanguage === 'en' ? 'en' : 'vi',
      initialized: true,
    });
  },
  setLanguage: async (language) => {
    set({ language });
    await AsyncStorage.setItem(LANGUAGE_KEY, language);
  },
}));

export const getLanguage = () => useLanguageStore.getState().language;

export const translate = (message: string, params?: Params) => {
  const translated = getLanguage() === 'en' ? en[message] || message : message;
  return interpolate(translated, params);
};

export const useTranslation = () => {
  const language = useLanguageStore(state => state.language);
  const setLanguage = useLanguageStore(state => state.setLanguage);
  const t = useCallback((message: string, params?: Params) => {
    const translated = language === 'en' ? en[message] || message : message;
    return interpolate(translated, params);
  }, [language]);

  return { language, setLanguage, t };
};
